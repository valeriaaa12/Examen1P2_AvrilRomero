    public int ratingequipos(Equipo e1, Equipo e2) {
        int diferencia;
        int probabilidad = 1;
        if (e1.getRating() > e2.getRating()) {
            diferencia = e1.getRating() - e2.getRating();
        } else {
            diferencia = e2.getRating() - e1.getRating();
        }
        if (diferencia <= 10) {
            probabilidad = diferencia * 4;
        } else {
            probabilidad = diferencia * 5;
        }
        return probabilidad;
    }

